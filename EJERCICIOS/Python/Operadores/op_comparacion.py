# OPERADORES DE COMPARACION #

# ==   -> es igual que
# !=   -> es distinto que
# <    -> es menor que
# >    -> es mayor que
# <=   -> es menor o igual que
# >=   -> es mayor o igual que

es_igual_que = 5 == 6

es_distinto_de = 5 != 6

mayor_que = 5 > 6

menor_que = 5 < 6

mayor_o_igual = 5 >= 6

menor_o_igual = 5 <= 6

